    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        baseDatos.conectar();
        try{
        Statement sta = baseDatos.getConexionBD().createStatement();
        String sql = "SELECT * FROM \"PERSONA\";";
        ResultSet respuesta = sta.executeQuery(sql);
        int edadmen15h=0;
        int edadmen15m=0;
        int edad16a20h=0;
        int edad16a20m=0;
        int edad21a30h=0;
        int edad21a30m=0;
        int edad31a40h=0;
        int edad31a40m=0;
        int edadmay40h=0;
        int edadmay40m=0;
        
        double edadmen15hTotal=0;
        double edadmen15mTotal=0;
        double edad16a20hTotal=0;
        double edad16a20mTotal=0;
        double edad21a30hTotal=0;
        double edad21a30mTotal=0;
        double edad31a40hTotal=0;
        double edad31a40mTotal=0;
        double edadmay40htotal=0;
        double edadmay40mTodal=0;
        
        while(respuesta.next()){
            int edad=respuesta.getInt(4);
            String genero=respuesta.getString(8);
            
            if(edad>15 && edad <=20 && genero.equals("MASCULINO")){ edad16a20h++; edad16a20hTotal+=respuesta.getDouble(7);}
            if(edad>15 && edad <=20 && genero.equals("FEMENINO")){ edad16a20m++;  edad16a20mTotal+=respuesta.getDouble(7);}
            if(edad>=21 && edad <=30 && genero.equals("MASCULINO")) {edad21a30h++; edad21a30hTotal+=respuesta.getDouble(7);}
            if(edad>=21 && edad <=30 && genero.equals("FEMENINO")) {edad21a30m++;  edad21a30mTotal+=respuesta.getDouble(7);}
            if(edad>=31 && edad <=40 && genero.equals("MASCULINO")){ edad31a40h++; edad31a40hTotal+=respuesta.getDouble(7);}
            if(edad>=31 && edad <=40 && genero.equals("FEMENINO")) {edad31a40m++;  edad31a40mTotal+=respuesta.getDouble(7);}
            if(edad>=41 && edad <=100 && genero.equals("MASCULINO")) {edadmay40h++;  edadmay40htotal+=respuesta.getDouble(7);}
            
            if(edad>40 && edad <=100 && genero.equals("FEMENINO")) {edadmay40m++;   edadmay40mTodal+=respuesta.getDouble(7);}
           
        }
        baseDatos.desconectar();
       
        if(edadmen15h==0) edadmen15h=1;
        if(edadmen15m==0) edadmen15m=1;
        if(edad16a20m==0) edad16a20m=1;
        if(edad16a20h==0) edad16a20h=1;
        if(edad21a30m==0) edad21a30m=1;
        if(edad21a30h==0) edad21a30h=1;
        if(edad31a40m==0) edad31a40m=1;
        if(edad31a40h==0) edad31a40h=1;
        if(edadmay40m==0) edadmay40m=1;
        if(edadmay40h==0) edadmay40h=1;
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(edadmen15mTotal/edadmen15m, "Mujeres", "menores a 15");
        dataset.setValue(edadmen15hTotal/edadmen15h, "Hombres", "menores a 15");
        dataset.setValue(edad16a20mTotal/edad16a20m, "Mujeres", "entre 16 y 20");
        dataset.setValue(edad16a20hTotal/edad16a20h, "Hombres", "entre 16 y 20");
        dataset.setValue(edad21a30mTotal/edad21a30m, "Mujeres", "entre 21 y 30");
        dataset.setValue(edad21a30hTotal/edad21a30h, "Hombres", "entre 21 y 30");
        dataset.setValue(edad31a40mTotal/edad31a40m, "Mujeres", "entre 31 y 40");
        dataset.setValue(edad31a40hTotal/edad31a40h, "Hombres", "entre 31 y 40");
        dataset.setValue(edadmay40mTodal/edadmay40m, "Mujeres", "mayores a 40");
        dataset.setValue(edadmay40htotal/edadmay40h, "Hombres", "mayores a 40");
        
        JFreeChart chart = ChartFactory.createBarChart3D
        ("Participacion por Genero","Genero", "Salario Promedio", 
        dataset, PlotOrientation.VERTICAL, true,true, false);
        chart.setBackgroundPaint(Color.cyan);
        chart.getTitle().setPaint(Color.blue); 
        CategoryPlot p = chart.getCategoryPlot(); 
        p.setRangeGridlinePaint(Color.green); 
        ChartFrame frame = new ChartFrame("Genero y salario", chart);
        frame.pack();
        frame.setVisible(true);
        
       }catch(SQLException ex){
       ex.printStackTrace();
       }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
      try {
            DefaultCategoryDataset line_chart_dataset = new DefaultCategoryDataset();
            baseDatos.conectar();
            List<SalarioEdad> lista=new ArrayList<SalarioEdad>();
            String sql="SELECT * FROM \"PERSONA\";";
            Statement sta=baseDatos.getConexionBD().createStatement();
            ResultSet resultado=sta.executeQuery(sql);
            while(resultado.next()){
                int edadPersona=resultado.getInt(4);
                SalarioEdad salarioEdad=new SalarioEdad();
                salarioEdad.setEdad(edadPersona);
                salarioEdad.setSumatoria(resultado.getDouble(7));
                lista.add(salarioEdad);
            }
            baseDatos.desconectar();
        
            for(SalarioEdad salarioEdad : lista){
           
        line_chart_dataset.addValue(salarioEdad.obtenerPromedio(), "Salario Promedio", Integer.toString(salarioEdad.getEdad()));
               
           
        }
            
            JFreeChart chart=ChartFactory.createLineChart("Salario Promedio",
                "Edad","Sueldo Promedio",line_chart_dataset,PlotOrientation.VERTICAL,
                true,true,false);
            ChartFrame frame = new ChartFrame("Pomedio Salario", chart);
            frame.pack();
            frame.setVisible(true);
            
            
        } catch (SQLException ex) {
            ex.printStackTrace();
           // Logger.getLogger(GeneradorPDF.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

