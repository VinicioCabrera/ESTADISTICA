    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      // Fuente de Datos
       DefaultPieDataset data = new DefaultPieDataset();
        baseDatos.conectar();
        try{
             
          Statement obj= baseDatos.getConexionBD().createStatement();
       String sql="SELECT * FROM\"PERSONA\";";
               ResultSet res=obj.executeQuery(sql);
               int edad15=0;
               int edad16hasta20=0;
               int edad21hasta30=0;
               int edad31hasta40=0;
               int edadmayor40=0;
               while(res.next()){
                   int edad=res.getInt(4);
                   if(edad<15)edad15 ++;
                   if(edad>15 && edad<=20)edad16hasta20 ++;
                   if(edad>15 && edad<=20)edad21hasta30 ++;
                   if(edad>15 && edad<=20)edad31hasta40 ++;
                   if(edad>40)edadmayor40 ++;
               }
               baseDatos.desconectar();
        data.setValue("EDAD DESDE LOS 10",edad15);
        data.setValue("EDAD ENTRE 10 Y 20", edad16hasta20);
        data.setValue("EDAD ENTRE 21 A 30", edad21hasta30);
        data.setValue("EDAD ENTRE 31 A 40", edad31hasta40);
        data.setValue("EDAD MAYOR A 41" , edadmayor40);
        // Creando el Grafico
        JFreeChart chart = ChartFactory.createPieChart(
         "ESTADISTICA DE EDADES", 
         data, 
         true, 
         true, 
         false);
 
        // Mostrar Grafico
        ChartFrame frame = new ChartFrame("EDADES", chart);
        frame.pack();
        frame.setVisible(true);
         }catch(SQLException ex){
             ex.printStackTrace();
         }
    }//GEN-LAST:event_jButton3ActionPerformed

